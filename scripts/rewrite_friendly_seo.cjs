async function main() {
  const files = Object.keys(seoContentMap);
  let updatedCount = 0;

  for (const file of files) {
    const filePath = path.join(calculatorsDir, file);
    if (!fs.existsSync(filePath)) {
      console.warn(`File not found: ${file}`);
      continue;
    }

    let content = fs.readFileSync(filePath, 'utf8');
    
    // Replace <InfoGuide ...> ... </InfoGuide> with the new content
    const infoGuideRegex = /<InfoGuide title="[^"]*">[\s\S]*?<\/InfoGuide>/;
    if (infoGuideRegex.test(content)) {
      // Preserve Indentation roughly, simple string replacement
      content = content.replace(infoGuideRegex, seoContentMap[file].trim());
      fs.writeFileSync(filePath, content, 'utf8');
      updatedCount++;
      console.log(`Updated ${file}`);
    } else {
      console.warn(`Could not find <InfoGuide> block in ${file}`);
    }
  }

  console.log(`Done. Updated ${updatedCount} files.`);
}

main().catch(console.error);
